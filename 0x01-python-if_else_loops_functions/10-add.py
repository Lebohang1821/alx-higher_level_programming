def add(a, b):
    """
    This function takes two numbers, 'a' and 'b', and returns their sum.

    Args:
        a (float or int): The first number.
        b (float or int): The second number.

    Returns:
        float or int: The sum of 'a' and 'b'.
    """
    return a + b
